#!/usr/bin/env node
/**
 * Generate llms.txt, nginx markdown route map, and stub .md files for bot-readable pages.
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";

const webRoot = resolve(import.meta.dirname, "..");
const publicDir = join(webRoot, "public");
const contentDir = join(webRoot, "content");
const siteOrigin = "https://www.nyxis.io";

const routes = JSON.parse(readFileSync(join(contentDir, "routes.json"), "utf8"));

function stubMarkdown(route) {
  const interactive = route.interactive
    ? "\n\n> **Note:** This page is an interactive browser demo. Full functionality requires JavaScript, SharedArrayBuffer (COOP/COEP), and a modern browser. The summary below describes what the live page tests.\n"
    : "";
  return `# ${route.title}

${route.description}${interactive}

- HTML (browser): ${siteOrigin}${route.path}
- Markdown (this document): ${siteOrigin}${route.markdown}
`;
}

for (const route of routes) {
  const filePath = join(publicDir, route.markdown.slice(1));
  mkdirSync(dirname(filePath), { recursive: true });
  if (!existsSync(filePath)) {
    writeFileSync(filePath, `${stubMarkdown(route)}\n`);
    console.log(`Created stub ${route.markdown}`);
  }
}

const llms = [
  "# Nyxis",
  "",
  "> Zero-copy binary serialization (NXS) — row, columnar, and PAX .nxb layouts.",
  "",
  "Static markdown versions of each page (no JavaScript required):",
  "",
  ...routes.flatMap((r) => [
    `- [${r.title}](${siteOrigin}${r.path}): ${siteOrigin}${r.markdown}`,
  ]),
  "",
  "## Agent discovery",
  "",
  `- API catalog: ${siteOrigin}/.well-known/api-catalog`,
  `- MCP server card: ${siteOrigin}/.well-known/mcp/server-card.json`,
  `- Agent skills: ${siteOrigin}/.well-known/agent-skills/index.json`,
  `- Specification: https://github.com/nyxis-io/nyxis/blob/main/SPEC.md`,
  "",
];
writeFileSync(join(publicDir, "llms.txt"), `${llms.join("\n")}\n`);
console.log("Wrote public/llms.txt");

const mapLines = [
  "# Generated by site/web/scripts/generate-page-markdown.mjs — do not edit.",
  "map $uri $markdown_uri {",
  '    default "";',
  ...routes.map((r) => `    ${r.path} ${r.markdown};`),
  "}",
];
const mapPath = resolve(webRoot, "../../docker/markdown-routes.map");
writeFileSync(mapPath, `${mapLines.join("\n")}\n`);
console.log(`Wrote ${mapPath}`);
